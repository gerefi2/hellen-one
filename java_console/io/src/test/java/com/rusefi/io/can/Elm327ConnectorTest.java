package com.gerefi.io.can;

import com.gerefi.io.can.elm.Elm327Connector;
import com.gerefi.util.HexBinary;
import org.junit.jupiter.api.Test;

import static org.junit.jupiter.api.Assertions.assertEquals;

public class Elm327ConnectorTest {
    @Test
    public void testBytesToString() {
        assertEquals("30 46 0D ",
                HexBinary.printHexBinary(Elm327Connector.byteToString(new byte[]{0xF}))
        );


        assertEquals("31 30 30 42 30 30 30 35 36 42 30 30 30 30 35 30 0D ",
                HexBinary.printHexBinary(Elm327Connector.byteToString(new byte[]{16, 11, 0, 5, 107, 0, 0, 80}))
        );

    }
}
